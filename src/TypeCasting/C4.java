package TypeCasting;

public class C4 {
    public static void main(String[] args) {
        /*
        double data tipinde bir degisken olusturunuz ve onu bir short degiskenine donusturunuz
        Sonrasinda bu short degiskenin degerini konsolda yazdiriniz
        Not: ciktiya dikkat ediniz ondalik bir deger olmayacaktir
         */
        double yas=10.0;
        short yeniYas=(short) yas;

        System.out.println(yeniYas);
    }
}
