package TypeCasting;

public class C3 {
    public static void main(String[] args) {
        /*
        double data tipinde bir degisken olusturunuz
        ve onu bir float degiskenine donusturunuz
         */
        double yas=10.0;
        float yeniYas=(float) yas;

        System.out.println(yeniYas);
        System.out.println(yas);
    }
}
