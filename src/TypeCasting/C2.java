package TypeCasting;

public class C2 {
    public static void main(String[] args) {
        /*
        long data tipinde bir degisken olusturunuz ve onu bir int degiskene donusturunuz
         */
        long yas = 10L;
        int yeniYas = (int) yas;
        System.out.println(yeniYas);
        System.out.println(yas);

    }
}
