package Variables;

public class C2 {
    public static void main(String[] args) {
        /*
        Herhangi bir 3 oge icin float,long ve integer data tipinde degiskenler olusturunuz
        Bu degiskenlerin degerlerinin carpimini konsolda yazdiriniz
         */

        float x = 15.99F;
        long y = 13L;
        int z = 12;
        System.out.println("Degiskenlerin Carpimlari : " + (x * y * z));
    }
}
